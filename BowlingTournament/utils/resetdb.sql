-- create tables
create table GAMESTATUS (
	gameStatusID varchar(10) not null,
	primary key (gameStatusID)
);

create table TOURNAMENTROUND (
	roundID varchar(5) not null,
	primary key (roundID)
);

create table TEAM (
	teamID int not null,
	teamName varchar(40),
	earnings int,
	primary key (teamID)
);

create table PLAYER (
	playerID int not null,
	teamID int,
	firstName varchar(40),
	lastName varchar(40),
	hometown varchar(40),
	province varchar(2),
	primary key (playerID),
	foreign key (teamID) references TEAM(teamID)
);

create table MATCHUP (
	matchID int not null,
	roundID varchar(5),
	matchgroup int,
	teamID int,
	score int,
	ranking int,
	primary key (matchID),
	foreign key (roundID) references TOURNAMENTROUND(roundID),
	foreign key (teamID) references TEAM(teamID)
);

create table GAME (
	gameID int not null,
	matchID int,
	gameNumber int,
	gameStatusID varchar(10),
	score int,
	balls varchar(80),
	primary key (gameID),
	foreign key (matchID) references MATCHUP(matchID),
	foreign key (gameStatusID) references GAMESTATUS(gameStatusID)
);

